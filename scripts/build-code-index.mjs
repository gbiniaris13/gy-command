#!/usr/bin/env node
/**
 * Build a code index for the Ask-the-Cockpit "what does this do" /
 * "where is X defined" tool (Tier 4d). Walks src/ + the repo's
 * top-level reference docs, captures path + line count + a clipped
 * version of the file content (configurable cap), and writes
 * src/lib/code-index.json. Next.js bundles JSON imports automatically
 * so the runtime never has to touch the filesystem.
 *
 * Run: node scripts/build-code-index.mjs
 * Auto-runs via the `prebuild` npm hook.
 *
 * The index is intentionally KEPT IN GIT so deploys are deterministic
 * and so PR diffs surface code-shape changes. Regenerate when source
 * shifts meaningfully — `prebuild` keeps it fresh on every deploy.
 */
import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

// Keep individual entries small enough that ~10 of them fit comfortably
// in a single LLM context turn. 8k chars × 10 = 80k chars ≈ 20k tokens.
const MAX_CHARS_PER_FILE = 8000;
const SKIP_DIRS = new Set([
  "node_modules", ".next", ".git", ".vercel", "dist", "build",
  "coverage", ".turbo", ".cache",
]);
const INCLUDE_EXTS = new Set([
  ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs",
  ".sql",  // migrations
  ".md",   // CLAUDE/ARCHITECTURE/DATA_MODEL/PLAYBOOKS/README
]);

// Roots to walk. Keep this tight — each path here is recursive.
const WALK_ROOTS = ["src", "scripts"];
const TOP_LEVEL_FILES = [
  "CLAUDE.md", "README.md", "AGENTS.md",
  "ARCHITECTURE.md", "DATA_MODEL.md", "PLAYBOOKS.md",
  "GY_COMMAND_OPERATIONS_MANUAL.md",
  "vercel.json", "package.json", "next.config.ts", "tsconfig.json",
];

/** @typedef {{ path: string, lines: number, bytes: number, content: string, truncated: boolean }} CodeEntry */

async function walkDir(dir, acc) {
  let entries;
  try {
    entries = await fs.readdir(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    const rel = path.relative(ROOT, full);
    if (entry.isDirectory()) {
      if (SKIP_DIRS.has(entry.name)) continue;
      await walkDir(full, acc);
    } else if (entry.isFile()) {
      const ext = path.extname(entry.name).toLowerCase();
      if (!INCLUDE_EXTS.has(ext)) continue;
      acc.push(rel);
    }
  }
}

async function readEntry(rel) {
  const abs = path.join(ROOT, rel);
  let buf;
  try {
    buf = await fs.readFile(abs);
  } catch {
    return null;
  }
  // Skip binary-looking files (shouldn't happen given INCLUDE_EXTS, but defensive)
  if (buf.includes(0)) return null;
  const raw = buf.toString("utf8");
  const lines = raw.split("\n").length;
  const truncated = raw.length > MAX_CHARS_PER_FILE;
  const content = truncated
    ? raw.slice(0, MAX_CHARS_PER_FILE) + `\n\n/* ...truncated; full file is ${lines} lines, ${raw.length} chars... */`
    : raw;
  return {
    path: rel.split(path.sep).join("/"), // forward slashes always
    lines,
    bytes: raw.length,
    content,
    truncated,
  };
}

async function main() {
  const paths = [];
  for (const r of WALK_ROOTS) {
    await walkDir(path.join(ROOT, r), paths);
  }
  for (const f of TOP_LEVEL_FILES) {
    const abs = path.join(ROOT, f);
    try {
      const stat = await fs.stat(abs);
      if (stat.isFile()) paths.push(f);
    } catch {
      // missing — skip silently
    }
  }
  paths.sort();

  /** @type {CodeEntry[]} */
  const entries = [];
  for (const p of paths) {
    const e = await readEntry(p);
    if (e) entries.push(e);
  }

  const index = {
    generated_at: new Date().toISOString(),
    file_count: entries.length,
    total_bytes: entries.reduce((acc, e) => acc + e.bytes, 0),
    max_chars_per_file: MAX_CHARS_PER_FILE,
    note: "Auto-generated by scripts/build-code-index.mjs. Do not edit by hand. Regenerate via `npm run build` (prebuild hook).",
    files: entries,
  };

  const out = path.join(ROOT, "src", "lib", "code-index.json");
  await fs.mkdir(path.dirname(out), { recursive: true });
  await fs.writeFile(out, JSON.stringify(index, null, 0)); // single-line for smaller diffs
  console.log(`[build-code-index] wrote ${entries.length} files (${(index.total_bytes / 1024).toFixed(1)} KiB total) → ${path.relative(ROOT, out)}`);
}

main().catch((e) => {
  console.error("[build-code-index] failed:", e);
  process.exit(1);
});
