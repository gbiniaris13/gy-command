// Pillar 3 — Greek Orthodox name-day calendar.
//
// Maps a first name (case + accent insensitive) to its name-day in
// the form "MM-DD". Coverage: ~120 most common Greek first names.
// Non-Greek names return null.
//
// Source: Greek Orthodox Church liturgical calendar (eortologio).
// For names with multiple feast days, the most popularly observed
// one is returned. Diminutives map to the canonical name (e.g.
// "Νίκος" → "Νικόλαος").

const NORMALIZE_MAP: Record<string, string> = {
  ά: "α", ί: "ι", ό: "ο", ύ: "υ", έ: "ε", ή: "η", ώ: "ω",
  Ά: "Α", Ί: "Ι", Ό: "Ο", Ύ: "Υ", Έ: "Ε", Ή: "Η", Ώ: "Ω",
  ϊ: "ι", ϋ: "υ", ΐ: "ι", ΰ: "υ",
};

function normalize(name: string): string {
  return name
    .trim()
    .toLowerCase()
    .split("")
    .map((c) => (NORMALIZE_MAP[c] ?? c).toLowerCase())
    .join("");
}

