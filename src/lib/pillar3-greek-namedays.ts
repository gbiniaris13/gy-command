/**
 * Look up the name day for a Greek first name.
 * Returns "MM-DD" or null if the name isn't in the calendar.
 */
export function nameDayFor(firstName: string | null | undefined): string | null {
  if (!firstName) return null;
  const norm = normalize(firstName);
  const canonical = DIMINUTIVES[norm] ?? norm;
  return NAME_DAYS[canonical] ?? NAME_DAYS[norm] ?? null;
}

/**
 * For a given month/day, return the canonical Greek names whose name-day
 * falls then. Used by the cron to query "whose name day is tomorrow".
 */
export function namesOnDay(month: number, day: number): string[] {
  const key = `${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`;
  return Object.entries(NAME_DAYS)
    .filter(([, v]) => v === key)
    .map(([k]) => k);
}
